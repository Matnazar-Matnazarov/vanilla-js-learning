// Window obyekti
console.log(window.innerWidth);
console.log(window.innerHeight);

// Template literals (interpolatsiya)
let name = "John";
let age = 30;
let message = `Mening ismim ${name}, yoshim ${age} da`;
console.log(message);

// Arifmetik operatorlar
let a = 10;
let b = 5;
console.log(a + b); // 15
console.log(a - b); // 5
console.log(a * b); // 50
console.log(a / b); // 2
console.log(a % b); // 0

// Mantiqiy operatorlar
console.log(true && false); // false
console.log(true || false); // true
console.log(!true);         // false

// Comparison operatorlar
console.log(5 > 3);   // true
console.log(5 < 3);   // false
console.log(5 === 5); // true
console.log(5 !== 3); // true

